//===---------------------------------------------------------------------===//
// ScopeOp
//===---------------------------------------------------------------------===//

void linalg::transform::ScopeOp::getSuccessorRegions(
    Optional<unsigned> index, ArrayRef<Attribute> operands,
    SmallVectorImpl<RegionSuccessor> &regions) {
  if (index)
    regions.emplace_back(getResults());
  else
    regions.emplace_back(&getBody());
}

